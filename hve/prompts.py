"""prompts.py — QA / Review プロンプト定数"""

from __future__ import annotations

# QA 質問票生成プロンプト
QA_PROMPT: str = """あなたはプロジェクトマネージャー兼品質保証担当です。
Issue に記述されたタスクの成果物に対して、確認すべき質問事項を 15〜100個リストアップしてください。
各質問は選択式（Yes/No または A/B/C 等）で回答できるようにフォーマットしてください。
質問票は Markdown のチェックリスト形式で作成してください。
回答の候補も提示し、推奨回答にはデフォルトマークを付けてください。"""

# QA 回答反映プロンプト（{user_answers} に回答内容を埋め込む）
QA_APPLY_PROMPT: str = """以下がユーザーの回答です:

{user_answers}

この回答に基づいて、Issue の成果物を更新してください。
回答されていない質問はデフォルト回答を採用してください。
変更があった箇所は差分がわかるように明示してください。"""

# Review プロンプト
REVIEW_PROMPT: str = """あなたはシニアアーキテクト兼コードレビュアーです。
Issue に記述されたタスクの目的を達成できているのかを、3回異なる観点でレビューしてください。

1回目: 機能的観点（要件充足性、ロジックの正確性）
2回目: 非機能的観点（パフォーマンス、セキュリティ、保守性）
3回目: プロジェクト横断的観点（他ステップとの整合性、ドキュメントの一貫性）

各観点で問題点を 15〜100個リストアップし、その修正案も提示してください。
最後に全ての問題を解決し、最終的な成果物を出力してください。"""

# Code Review Agent レビュー結果への対応プロンプト（{review_comments} に埋め込む）
CODE_REVIEW_AGENT_FIX_PROMPT: str = """GitHub Copilot Code Review Agent から以下のレビューコメントが返されました:

{review_comments}

上記の全ての指摘事項について:
1. 各指摘の内容を確認し、修正が必要か判断してください
2. 修正が必要な箇所は全て修正してください
3. 修正内容の概要を箇条書きで報告してください"""
